public class sumofnumberGivenNumber {
    public static void main(String[] args) {
        // Input : Number1 = 12 , Number2 = 15 Output : 54
        int number1 =5;
        int number2=10;

        int sum=0;
        for(int i=number1;i<=number2;i++){
            // System.out.println(i);
            sum+=i;
        }
        System.out.println(sum);
    }
}
